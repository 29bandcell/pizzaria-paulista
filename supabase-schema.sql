create or replace function public.touch_paulista_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.updated_at = now();
  return new;
end;
$$;

drop trigger if exists paulista_products_touch_updated_at on public.paulista_products;

create trigger paulista_products_touch_updated_at
before update on public.paulista_products
for each row execute function public.touch_paulista_updated_at();
